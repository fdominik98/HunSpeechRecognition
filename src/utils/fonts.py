from customtkinter import CTkFont

def button_font():
    return CTkFont(size=14, family='Arial', weight="bold")

def small_button_font():
    return CTkFont(size=13, family='Arial', weight="bold")

def label_font():
    return CTkFont(size=15, family='Arial',  weight="bold")

def textbox_font():
    return CTkFont(size=14, family='Arial', weight='normal')